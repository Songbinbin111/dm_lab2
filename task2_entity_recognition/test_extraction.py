def clean_poi(poi):
    # 去除括号内容
    poi = re.sub(r'[\(（][^\)）]+[\)）]', '', poi)
    poi = poi.strip()
    
    # 处理 "红门游客中心红门" -> "红门"
    # 简单策略：如果后半部分包含前半部分，或者重复
    # 更好的策略：使用 jieba 分词，看是否包含已知的 POI
    
    # 针对泰山数据的特定规则：如果看起来像 "A游客中心A" 或 "AA"
    if len(poi) > 4:
        # 尝试找出重复部分
        mid = len(poi) // 2
        for i in range(2, mid + 2):
            suffix = poi[-i:]
            if poi.startswith(suffix) or (suffix in poi[:-i]):
                return suffix
            
            prefix = poi[:i]
            if poi.endswith(prefix) or (prefix in poi[i:]):
                return prefix
                
    return poi

def extract(text):
    # 移除时间标记 [上午] 等
    text = re.sub(r'\[.*?\]', '', text)
    
    # 预处理：去除括号内的时间信息，避免干扰
    # 但要注意不要误删
    
    parts = text.split('→')
    pois = []
    for p in parts:
        cleaned = clean_poi(p)
        if cleaned:
            pois.append(cleaned)
    return pois

print(extract(text))
